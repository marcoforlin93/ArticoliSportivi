    private void jComboBoxCategoryArticleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCategoryArticleActionPerformed
        // rimuove tutti i tipi di articoli dal jComboBox
        jComboBoxTypeArticle.removeAllItems();

        // inserisce i tipi di articolo della categoria scelta:
        Set<String> articleTypes = warehouse.getArticleTypes(Sport.valueOf(jComboBoxCategoryArticle.getSelectedItem().toString()));
        articleTypes.forEach((String myString) -> {
            jComboBoxTypeArticle.addItem(myString);
        });
    }//GEN-LAST:event_jComboBoxCategoryArticleActionPerformed

    private void jComboBoxTypeArticleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxTypeArticleActionPerformed
//        jListInformations.
        updateJList();
    }//GEN-LAST:event_jComboBoxTypeArticleActionPerformed

    private void updateJList() {
        jListInformations.removeAll();

        DefaultListModel model;
        model = new DefaultListModel<>();

        try {
            Set<String> articleTypes = warehouse.getArticleTypesInformation(Sport.valueOf(jComboBoxCategoryArticle.getSelectedItem().toString()), jComboBoxTypeArticle.getSelectedItem().toString());
            articleTypes.forEach((String myString) -> {
                model.addElement(myString);
            });

        } catch (NullPointerException e) {
            
        }
        

        
        jListInformations.setModel(model);
        jListInformations.setSelectedIndex(0);
    }

